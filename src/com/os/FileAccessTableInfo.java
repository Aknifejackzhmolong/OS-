package com.os;

public class FileAccessTableInfo {
    private int begin;
    private int end;
    private String OperateSystm;

    public int getBegin() {
        return begin;
    }

    public void setBegin(int begin) {
        this.begin = begin;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public String getOperateSystm() {
        return OperateSystm;
    }

    public void setOperateSystm(String operateSystm) {
        OperateSystm = operateSystm;
    }
}
